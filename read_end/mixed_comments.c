#include <stdio.h>
#include <stdlib.h>

// Файл демонстрирует сочетание однострочных и блочных комментариев

/* Заголовочный комментарий:
 * В этом примере показываются разные места размещения комментариев
 * и их совместное использование.
 */

static int helper_sum(int a, int b) { /* Возвращает сумму двух чисел */
    return a + b; // Короткое пояснение справа от выражения
}

static void print_range(int n) {
    /* Вывод всех i из диапазона [0, n) */
    for (int i = 0; i < n; ++i) {
        printf("i = %d\n", i);
    }
}

int main(void) {
    int a = 2, b = 5; // Инициализация переменных

    /* Вызов вспомогательной функции и печать результата */
    int s = helper_sum(a, b);
    printf("Sum = %d\n", s);

    // Временное отключение целого блока с помощью блочного комментария:
    /*
    print_range(3);
    */

    // Альтернатива — отключение одной строки:
    // print_range(1);

    return EXIT_SUCCESS; // успешное завершение
}
