#include <stdio.h>
#include <stdlib.h>

/*
 * Файл: block_comments.c
 * Назначение: Показать применение блочных комментариев в языке C.
 *
 * Ключевые моменты:
 *  - Блочные комментарии начинаются с символов '/*' и заканчиваются на '
 *  - Они могут занимать несколько строк и подходят для больших описаний.
 *  - Такие комментарии НЕЛЬЗЯ вкладывать друг в друга (стандарт C этого не допускает).
 *  - Удобны для временного выключения фрагментов кода.
 */

/* Пример простой функции с блочным комментарием над объявлением */
static int multiply(int a, int b) {
    return a * b; /* Краткий блочный комментарий в конце выражения допустим */
}

int main(void) {
    /* Инициализация данных */
    int x = 4;
    int y = 7;

    /*
     * Демонстрация работы функции multiply.
     * Здесь мы умножаем x и y и печатаем результат.
     */
    int prod = multiply(x, y);
    printf("Product = %d\n", prod);

    /* Временное отключение фрагмента кода с помощью блочного комментария
    printf("Этот вывод временно отключён.\n");
    int z = multiply(2, 3);
    printf("z = %d\n", z);
    */

    /* Пояснение:
     * Следующий блок иллюстрирует невозможность вложения:
     * НЕЛЬЗЯ писать так: /* вложенный комментарий / — это приведёт к ошибке парсинга.
     * Вместо этого используйте последовательные однострочные // или разбейте блок.
     */

    /* Финальный статус завершения */
    return EXIT_SUCCESS;
}
