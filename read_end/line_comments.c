// Возвращает максимум из двух целых
static int max2(int a, int b) { return (a > b) ? a : b; } // тернарный оператор

int main(void) {
    // Инициализация переменных
    int a = 10; // первое число
    int b = 42; // второе число

    // Подсчёт максимума и вывод результата
    int m = max2(a, b); // ожидается 42
    printf("max(%d, %d) = %d\n", a, b, m);

    // Временное отключение одной строки кода с помощью //
    // printf("Эта строка отключена однострочным комментарием\n");

    // Возврат кода успешного завершения
    return EXIT_SUCCESS; // завершение
}
